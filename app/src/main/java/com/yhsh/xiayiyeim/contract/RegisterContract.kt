interface RegisterContract {
    interface Presenter {
        //用户名,密码,确认密码
        fun register(username: String, password: String, confirmPassword: String)
    }

    interface View {
        //用户名不正确
        fun userNameError()

        //密码不正确
        fun passwordError()

        //确认密码不正确
        fun confirmPasswordError()

        //开始注册
        fun startRegister()

        //注册成功
        fun onRegisterSuccess()

        //注册失败
        fun onRegisterFail()
    }
}