interface LoginContract {
    interface Presenter : BasePresenter {
        //登录的方法
        fun login(userName: String, password: String)
    }

    interface View {
        //y用户名出错的情况
        fun onUserNameError()

        //密码出错的情况
        fun onPasswordError()

        //开启登录
        fun onStartLogin()

        //登录成功
        fun onLoggedSuccess()

        //登录失败
        fun onLoggedFail()
    }
}